// Descrição   : Comparar dois valores e verificar se são iguais ou qual dos dois são maiores!
let valorA = parseInt(prompt("Informe o primeiro valor"))
let valorB = parseInt(prompt("Informe o segundo valor"))
if ( valorA == valorB){
   alert("Números iguais")
} else if(valorA > valorB){
      alert("Primeiro é maior")
} else{
      alert("Segundo é maior")
}




